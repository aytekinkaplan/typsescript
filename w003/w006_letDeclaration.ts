//let declarations
// The let keyword is similar to the var keyword. The var declaration has some problems in solving programs,
// so ES6 introduced let keyword to declare a variable in TypeSript and JavaScript. The let keyword has some
// restriction in scoping in comparison of the var keyword.
//
// The let keyword can enhance our code readability and decreases the chance of programming error.
//
// The let statement are written as same syntax as the var statement:

/*
  var declaration:
var b = 50;
  let declaration:
let b = 50;
*/

//The key difference between var and let is not in the syntax, but it differs in the semantics.
// The Variable declared with the let keyword are scoped to the nearest enclosing block which
// can be smaller than a function block.