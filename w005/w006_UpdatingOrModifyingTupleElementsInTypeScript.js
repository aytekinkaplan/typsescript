// **Updating or Modifying Tuple Elements in TypeScript**
// Tuple elements in TypeScript can be updated or modified by assigning new values to specific indices.
// **Example: Modifying Tuple Elements**
var tuple = ['hello', 10, true];
tuple[0] = 'world';
tuple[1] = 20;
console.log("Modified tuple:", tuple); // Output: Modified tuple: ['world', 20, true]
// In this example, the first element of the tuple is modified to 'world' and the second element is modified to 20.
// The modified tuple is then logged to the console.
// By assigning new values to specific indices, you can update or modify tuple elements in TypeScript.
